// Integer number
var n = 1234;
console.log('Integer Number:', n, '\n');

// floting number
var f = 23.44;
console.log("Floating Number:", f, '\n');

// Floating to integer
var flotingNumber = Number(23.44);
var convertedNumber = Number.parseInt(flotingNumber);
console.log('Floating to integer number:', convertedNumber, '\n');


// we can write a number as like it
var number1 = Number(45);
console.log('Number:', number1, '\n');

// String to number
var stringToNumber = Number('242');
console.log('String to number', stringToNumber, '\n');


// Max Number 
console.log(Number.MAX_VALUE);

// Min number 
console.log(Number.MIN_SAFE_INTEGER);

// Infinity
console.log(1/0);

// NaN
console.log('abc' * 10);
